let currentLang = 'es';

function toggleLanguage() {
    currentLang = currentLang === 'es' ? 'en' : 'es';
    updateContent();
    document.getElementById('lang-toggle-btn').textContent = currentLang === 'es' ? 'English' : 'Español';
}

